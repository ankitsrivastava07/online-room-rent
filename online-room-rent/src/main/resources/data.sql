created_by INT NOT NULL,
authorities INT,
CONSTRAINT FKe6hmka5t4wedfb77k1afcnodw FOREIGN KEY (created_by) REFERENCES ADMIN(id),
PRIMARY KEY(id)
);

CREATE TABLE USERS_AUTHORITIES(
id INT,
active boolean NOT NULL,
